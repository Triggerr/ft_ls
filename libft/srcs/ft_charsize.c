int		ft_charsize(unsigned char c)
{
	if ((c & 0b11110000) == 0b11110000)
		return (4);
	else if ((c & 0b11100000) == 0b11100000)
		return (3);
	else if ((c & 0b11000000) == 0b11000000)
		return (2);
	else if ((c & 0b1000000) == 0b10000000)
		return (0);
	else
		return (1);
}
